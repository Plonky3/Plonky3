/// Internal (partial round) layer for KoalaBear Poseidon.
pub type PoseidonInternalLayerKoalaBear<const WIDTH: usize> =
    PoseidonInternalLayerMonty31<KoalaBearParameters, WIDTH, KoalaBearPoseidonParameters>;

/// External (full round) layer for KoalaBear Poseidon.
pub type PoseidonExternalLayerKoalaBear<const WIDTH: usize> =
    PoseidonExternalLayerMonty31<KoalaBearParameters, MDSKoalaBearData, WIDTH>;

/// S-box degree for KoalaBear Poseidon.
///
/// Since p - 1 = 127 * 2^24, gcd(3, p-1) = 1, so x^3 is a valid S-box.
const KOALABEAR_S_BOX_DEGREE: u64 = 3;

/// The Poseidon permutation for KoalaBear.
///
/// Acts on arrays of the form `[KoalaBear; WIDTH]` or `[KoalaBear::Packing; WIDTH]`.
pub type PoseidonKoalaBear<const WIDTH: usize> = Poseidon<
    KoalaBear,
    PoseidonExternalLayerKoalaBear<WIDTH>,
    PoseidonInternalLayerKoalaBear<WIDTH>,
    WIDTH,
    KOALABEAR_S_BOX_DEGREE,
>;

/// Generic Poseidon linear layers for KoalaBear.
