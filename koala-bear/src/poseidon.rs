/// Can act on `[A; WIDTH]` for any ring implementing `Algebra<KoalaBear>`.
pub type GenericPoseidonLinearLayersKoalaBear =
    GenericPoseidonLinearLayersMonty31<KoalaBearParameters, KoalaBearPoseidonParameters>;

