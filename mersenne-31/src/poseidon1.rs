//! Poseidon1 permutation for Mersenne31.
//!
//! This module provides the Poseidon1 hash permutation instantiated for the
//! Mersenne31 field (p = 2^31 - 1). It uses the generic Poseidon1 implementation
//! with Karatsuba MDS convolution from `MdsMatrixMersenne31`.
//!
//! # MDS Matrix
//!
//! The MDS matrix is a **circulant** matrix sourced from the MDS crate.
//! At runtime, it is applied via fast Karatsuba convolution (sub-O(t^2)).
//! During initialization only, it is expanded to dense form for the
//! sparse matrix decomposition of partial rounds.
//!
//! # Round Constants
//!
//! Generated by the Grain LFSR (Poseidon1 paper, Appendix E) with SBOX=0 (x^alpha encoding).

use p3_mds::util::first_row_to_first_col;
use p3_poseidon1::{
    Poseidon1, Poseidon1Constants, Poseidon1ExternalLayerGeneric, Poseidon1InternalLayerGeneric,
};

use crate::mds::{MATRIX_CIRC_MDS_16_SML_ROW, MATRIX_CIRC_MDS_32_MERSENNE31_ROW};
use crate::{MdsMatrixMersenne31, Mersenne31};

/// S-box degree for Mersenne31 Poseidon1.
///
/// Since `p - 1 = 2 * 3^2 * 7 * 11 * 31 * 151 * 331`, both 3 and 4 share factors
/// with `p - 1`. The smallest valid exponent satisfying `gcd(alpha, p - 1) = 1` is 5.
pub const MERSENNE31_POSEIDON1_S_BOX_DEGREE: u64 = 5;

/// Number of full rounds per half for Mersenne31 Poseidon1 (`RF / 2`).
///
/// The total number of full rounds is `RF = 8` (4 beginning + 4 ending).
/// Follows the Poseidon1 paper's security analysis (Section 5.4) with a +2 RF margin.
pub const MERSENNE31_POSEIDON1_HALF_FULL_ROUNDS: usize = 4;

/// Number of partial rounds for Mersenne31 Poseidon1 (width 16).
///
/// Derived from the interpolation bound in the Poseidon1 paper (Eq. 3):
///
///   R_interp >= ceil(min{kappa, n} / log_2(alpha)) + ceil(log_alpha(t)) - 5
///            = ceil(31 / log_2(5)) + ceil(log_5(16)) - 5 = 14 + 2 - 5 = 11
///
/// With the +7.5% security margin: ceil(1.075 * 11) = 12. After brute-force
/// optimization over all security constraints: R_P = 14.
pub const MERSENNE31_POSEIDON1_PARTIAL_ROUNDS_16: usize = 14;

/// Number of partial rounds for Mersenne31 Poseidon1 (width 32).
///
/// With the +7.5% security margin and brute-force optimization: R_P = 30.
pub const MERSENNE31_POSEIDON1_PARTIAL_ROUNDS_32: usize = 30;

/// Generic (non-SIMD) Poseidon1 permutation for Mersenne31.
///
/// Uses the platform-independent Poseidon1 implementation with Karatsuba
/// MDS convolution via `MdsMatrixMersenne31`.
pub type Poseidon1Mersenne31Generic<const WIDTH: usize> = Poseidon1<
    Mersenne31,
    Poseidon1ExternalLayerGeneric<Mersenne31, MdsMatrixMersenne31, WIDTH>,
    Poseidon1InternalLayerGeneric<Mersenne31, WIDTH>,
    WIDTH,
    MERSENNE31_POSEIDON1_S_BOX_DEGREE,
>;

/// Poseidon1 permutation for Mersenne31.
///
