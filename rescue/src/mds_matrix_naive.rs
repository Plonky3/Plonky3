fn u32_matrix_to_m31_matrix<const SIZE: usize>(
    matrix: [[u32; SIZE]; SIZE],
) -> [[Mersenne31; SIZE]; SIZE] {
    matrix
        .iter()
        .map(|row| {
            row.iter()
                .map(|&x| Mersenne31::from_canonical_u32(x))
                .collect_vec()
                .try_into()
                .unwrap()
        })
        .collect_vec()
        .try_into()
        .unwrap()
}

